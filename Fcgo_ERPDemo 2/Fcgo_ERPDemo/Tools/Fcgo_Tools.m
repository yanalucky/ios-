    NSDate *endDate = [NSDate dateWithTimeIntervalSince1970:endTime];
    
    NSDate *currentDate = [NSDate date];
    if ([currentDate compare:startDate] == NSOrderedDescending && [currentDate compare:endDate] == NSOrderedAscending) {
        return 1;
    }
